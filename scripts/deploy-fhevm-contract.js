        const wallet = new ethers.Wallet(PRIVATE_KEY, provider);
        
        console.log("âœ… Provider connected");
        console.log("âœ… Wallet address:", wallet.address);
        
        // 2. Create FHEVM instance
        console.log("ğŸ” Creating FHEVM instance...");
